package shieldtjava;

/**
 * В данном примере метод getValue () неявно считается абстрактным и единственным,
 * определяемым в интерфейсе MyNumber. Следовательно, интерфейс
 * MyNumber является функциональным, а его функция определяется методом
 * getValue ().
 */
public interface MyNumber {
    double getValue();
}
